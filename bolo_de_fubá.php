    <h1>Bolo de Fubá</h1>
    <p>Esta é uma receita deliciosa de bolo de fubá.</p>
    <ul>
        <li>Ingredientes:</li>
        <ul>
            <li>2 xícaras de açúcar</li>
            <li>2 xícaras de farinha de trigo</li>
            <li>1 xícara de fubá</li>
            <li>1 xícara de óleo</li>
            <li>4 ovos</li>
            <li>1 xícara de leite</li>
            <li>1 colher de sopa de fermento em pó</li>
        </ul>
        <li>Modo de preparo:</li>
        <ol>
            <li>Pré-aqueça o forno a 180°C.</li>
            <li>Em uma tigela, misture o açúcar, a farinha de trigo, o fubá e o óleo.</li>
            <li>Adicione os ovos e misture bem.</li>
            <li>Adicione o leite e misture novamente.</li>
            <li>Adicione o fermento em pó e misture até obter uma massa homogênea.</li>
            <li>Despeje a massa em uma forma untada e enfarinhada.</li>
            <li>Asse por cerca de 40 minutos ou até que um palito inserido no centro do bolo saia limpo.</li>
            <li>Deixe esfriar antes de servir.</li>
        </ol>
    </ul>
