    <h1>Bolo de Cenoura</h1>
    <p>Esta é uma receita deliciosa de bolo de cenoura.</p>
    <ul>
        <li>Ingredientes:</li>
        <ul>
            <li>3 cenouras médias</li>
            <li>3 ovos</li>
            <li>1 xícara de óleo</li>
            <li>2 xícaras de açúcar</li>
            <li>2 xícaras de farinha de trigo</li>
            <li>1 colher de sopa de fermento em pó</li>
        </ul>
        <li>Modo de preparo:</li>
        <ol>
            <li>Pré-aqueça o forno a 180°C.</li>
            <li>No liquidificador, bata as cenouras, os ovos e o óleo até obter uma mistura homogênea.</li>
            <li>Em uma tigela, misture o açúcar, a farinha de trigo e o fermento em pó.</li>
            <li>Adicione a mistura do liquidificador à tigela e misture bem.</li>
            <li>Despeje a massa em uma forma untada e enfarinhada.</li>
            <li>Asse por cerca de 40 minutos ou até que um palito inserido no centro do bolo saia limpo.</li>
            <li>Deixe esfriar antes de servir.</li>
        </ol>
    </ul>
