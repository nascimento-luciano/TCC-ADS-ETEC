    <h1>Bolo de Milho</h1>
    <p>Esta é uma receita deliciosa de bolo de milho.</p>
    <ul>
        <li>Ingredientes:</li>
        <ul>
            <li>1 lata de milho verde</li>
            <li>1/2 xícara de óleo</li>
            <li>3 ovos</li>
            <li>1 xícara de açúcar</li>
            <li>1 xícara de farinha de trigo</li>
            <li>1/2 xícara de fubá</li>
            <li>1 colher de sopa de fermento em pó</li>
        </ul>
        <li>Modo de preparo:</li>
        <ol>
            <li>Pré-aqueça o forno a 180°C.</li>
            <li>No liquidificador, bata o milho verde, o óleo, os ovos e o açúcar até obter uma mistura homogênea.</li>
            <li>Em uma tigela, misture a farinha de trigo, o fubá e o fermento em pó.</li>
            <li>Adicione a mistura do liquidificador à tigela e misture bem.</li>
            <li>Despeje a massa em uma forma untada e enfarinhada.</li>
            <li>Asse por cerca de 40 minutos ou até que um palito inserido no centro do bolo saia limpo.</li>
            <li>Deixe esfriar antes de servir.</li>
        </ol>
    </ul>
